                <p>This will be a flagship program under SSIP to scout, engage and leverage best mentors across domains and region to help the start-ups through I Hub and allied programs of SSIP. As mentorship plays a pivotal role in giving both direction and support to early stage innovators and start-ups, SSIP cell at education department will take a comprehensive approach to enable that through below steps.</p>

                <b>SSIP cell at education department will take a comprehensive approach to enable that through below steps.</b>

                <ul class="show_list">
                    <li>Program design</li>
                    <li>State level mentor board/platform involving best mentors</li>
                    <li>Outreach to mentors through portal, mailers, newspaper advertisement,referal</li>
                    <li>Scrutiny and finalizing the list</li>
                    <li>Give letter of engagement</li>
                    <li>Mentorship network through SSIP portal and engagement</li>
                    <li>Design and execute pro bono and at cost mentorship systems</li>
                    <li>Tie up with Best mentorship platforms and collaborative efforts</li>
                    <li>Awards/citations to best mentors</li>
                </ul>

