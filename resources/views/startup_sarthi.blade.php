                <b>Broadly the Start-up Sathi Programme will cater to beneficiaries in three layers as below:</b>
                
                <ol>
                    <li>Proof of Concept Design Stage/Ideation Phase</li>
                    <li>Incubation Stage/Product Market Fit Segment</li>
                    <li>Growth Stage Start-ups</li>
                </ol>

                <b>Broadly the Start-up Sathi/Mentors will be empanelled in three segments.</b><br>

                <p><span class="head">1. Start-up Sathi : Advisor</span>
                Such person in general has a depth of knowledge, experience, and connections. This is a person who has been publicly acclaimed for their success and who is willing to impart their wisdom and advice on people who are at an earlier stage of their career.</p>

                <p><span class="head">2. Start-up Sathi : Expert</span>
                “Expert” mentors can help the start-up/innovator solve a very specific problem it is facing. This is the type of mentor start-up turn to when it need deep knowledge on a particular subject. Start-up Sathi may vary depending upon Objectives of mentoring, Levels of time commitment (2/4/8 hours per month) to the mentees,Types of resource commitments (advice/contacts/ funds/ infrastructure etc.) to the mentoring process.</p>

                <p><span class="head">3. Start-up Sathi : Peer</span>
                Such persons are someone else who is better suited to give you advice on more day-to-day operational concerns. Generally a Peer mentor is someone who has been in mentee’s shoes quite recently. They’re on a similar path, just a few steps ahead of mentee.In contrast with Advisors, generally the Peer mentors do not tell the mentee precisely what the start-up/innovator need to do. Instead, they tell it how they approached a problem or how they’d do something differently if they could do it over again. But these lessons are extremely helpful and can definitely save start-up a lot of time.</p>

                <b>The mentorship pedagogy</b>
                
                <ol>
                    <li>One to One Mentorship</li>
                    <li>One to Many (Group) Mentoring</li>
                    <ul>
                        <li>Sector/Domain Specific</li>
                        <li>Sector Agonistic</li>
                    </ul>
                </ol>

                <b>Broad role of Start-up Sathi/Mentors</b>

                <ol>
                    <li>Give Technical guidance/inputs related with the innovation/start-up</li>
                    <li>Hold a mirror to Start-up/Enterprise of mentee</li>
                    <li>Ask the mentee tough questions</li>
                    <li>Enable the start-up/innovator to take a step back and see the big picture</li>
                    <li>Opens doors for the innovator/start-up</li>
                    <li>Push the bar higher and help you deliver optimum result</li>
                    <li>Share his/her value system</li>
                    <li>Motivate the mentee</li>
                    <li>Share heuristic level insights from own experience</li>
                    <li>Help the mentee to deliver value in time bound manner</li>
                </ol>

                <b>The nature of mentoring can vary across start-ups/mentors and over time. It can potentially encompass:</b>

                <ol>
                    <li>Advice to start-up on Value Proposition Design, Product design and allied needs.</li>
                    <li>Advice on start-up related strategic and operational issues across areas like business model/plan, start up team, funding options, scaling up, handling issues among founding team etc.</li>
                    <li>Access to contacts for sales, recruitment, tie-ups, PR</li>
                    <li>Access to funds (debt or equity) through self or through contacts to angel network, lending agencies, VCs etc.</li>
                    <li>Access to infrastructure – Hard infrastructure (office space, IT requirements) & Soft infrastructure (legal services, accounts/payroll, IP, research</li>
                </ol>

                <b>Benefits and Expectations from Start-up Sathi</b>

                <p>The Mentors get to benefit from the association with the Start-up Sathi programme in the following ways –</p>

                <ol>
                    <li>Give back to society and have the satisfaction of aiding the growth of an entrepreneurial venture with just a small time commitment</li>
                    <li>Chance to apply domain expertise in a different context from the mentor’s own day to day routine</li>
                    <li>Chance to apply skills unutilized in jobs</li>
                    <li>Official recognition as mentor/Start-up Sathi from I-Hub/SSIP</li>
                </ol>

                <b>SSIP/I-Hub has the following expectations from the mentors</b>

                <ol>
                    <li>Be confidential regarding all proprietary information shared by the Mentee</li>
                    <li>Fulfil commitment in terms of time and other resources</li>
                    <li>The mentor would leverage his/her network to support the start-up</li>
                    <li>Timely response to queries and requests from SSIP/I Hub and mentees</li>
                </ol>

                <b>Benefits of Mentee</b>

                <ol>
                    <li>Mentee can meet subject matter experts with practical industry and entrepreneurial experience</li>
                    <li>Mentors/Start-up Sathi could help in networking with potential business opportunities and investors, refining the business model, creating strategy and short term road map, monitoring company performance etc.</li>
                    <li>Mentors/ Start-up Sathi act as a sounding board for all decisions being contemplated by the mentee</li>
                    <li>Mentorship can also help you hone your soft-skills and equip you better in dealing with clients, handling pressure situations, managing your time effectively et al</li>
                    <li>Each mentee’s requirements are different and therefore the mentoring can be tailor-made to suit mentee’s requirements</li>
                </ol>

                <b>Commitment from Mentee</b>

                <ol>
                    <li>Mentee should be completely committed and dedicated in his/her pursuit of the idea.</li>
                    <li>Mentee shall be expected to be open about all the issues with the mentor, take the constructive criticism (if ever) from the mentor in the right spirit and incorporate the mentor’s feedback wherever feasible</li>
                    <li>The onus shall be on the mentee to make the mentorship process work passion, focus, dedication, openness, punctuality and humility are key characteristics that a mentor would like to see</li>
                    <li>It is most important to keep SSIP Cell/I Hub and the mentors engaged and involved with you regular updates shall be expected.</li>
                    <li>Mentee should adhere to best ethical standards in the process.</li>
                </ol>

                <b>Pre requisite for any mentee to benefit from Start-up Sathi Programme</b>

                <ol>
                    <li>Interested in developing their Innovation/Start-up.</li>
                    <li>Able to take responsibility for their own development</li>
                    <li>Open to receiving feedback</li>
                    <li>Willing to accept challenges</li>
                    <li>Positive about change and growth</li>
                    <li>Able to set goals and work towards them</li>
                    <li>Committed to working through on-the-job issues</li>
                </ol>
